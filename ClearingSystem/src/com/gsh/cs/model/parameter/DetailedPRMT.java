package com.gsh.cs.model.parameter;

import java.math.BigDecimal;

public class DetailedPRMT {
	/**
	 * 应收、应付、佣金账目明细
	 */
	private String exchangeNo;//编号
	private String accountNo;//账户编号
	private BigDecimal payAmount;//金额
	private String theOperator;//操作人
	private String currencyType;//货币
	private BigDecimal payBayAmount;//总金额
	private String paymentMethod;//收款方式
	
	
	public String getCurrencyType() {
		return currencyType;
	}
	public void setCurrencyType(String currencyType) {
		this.currencyType = currencyType;
	}
	public String getExchangeNo() {
		return exchangeNo;
	}
	public void setExchangeNo(String exchangeNo) {
		this.exchangeNo = exchangeNo;
	}
	
	public String getAccountNo() {
		return accountNo;
	}
	public void setAccountNo(String accountNo) {
		this.accountNo = accountNo;
	}
	public BigDecimal getPayAmount() {
		return payAmount;
	}
	public void setPayAmount(BigDecimal payAmount) {
		this.payAmount = payAmount;
	}
	public String getTheOperator() {
		return theOperator;
	}
	public void setTheOperator(String theOperator) {
		this.theOperator = theOperator;
	}
	public BigDecimal getPayBayAmount() {
		return payBayAmount;
	}
	public void setPayBayAmount(BigDecimal payBayAmount) {
		this.payBayAmount = payBayAmount;
	}
	public String getPaymentMethod() {
		return paymentMethod;
	}
	public void setPaymentMethod(String paymentMethod) {
		this.paymentMethod = paymentMethod;
	}
	
}
