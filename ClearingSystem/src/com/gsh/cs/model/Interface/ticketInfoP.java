package com.gsh.cs.model.Interface;

public class ticketInfoP {
	private String exchangeSingle;// 交换单号
	private double sellingTotal;// 销售总价
	private String noticeNo;// 通知单
	private String issuer;// 业务员
	private String route;// 航段

	public String getExchangeSingle() {
		return exchangeSingle;
	}

	public void setExchangeSingle(String exchangeSingle) {
		this.exchangeSingle = exchangeSingle;
	}

	public double getSellingTotal() {
		return sellingTotal;
	}

	public void setSellingTotal(double sellingTotal) {
		this.sellingTotal = sellingTotal;
	}

	public String getNoticeNo() {
		return noticeNo;
	}

	public void setNoticeNo(String noticeNo) {
		this.noticeNo = noticeNo;
	}

	public String getIssuer() {
		return issuer;
	}

	public void setIssuer(String issuer) {
		this.issuer = issuer;
	}

	public String getRoute() {
		return route;
	}

	public void setRoute(String route) {
		this.route = route;
	}

}
